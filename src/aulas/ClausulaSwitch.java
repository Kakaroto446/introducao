package aulas;

public class ClausulaSwitch {

	public static void main(String[] args) {
		int numero = 1;
		switch( numero ) {
		case 1 :
			System.out.println( "um" );
			break;
		case 2 :
			System.out.println( "dois" );
			break;
		case 3 :
			System.out.println( "tres" );
			break;
		default :
			System.out.println( "nenhum" );
			break;
		}

	}

}
